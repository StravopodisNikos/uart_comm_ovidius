#define SYNCED        111 // 01101111 (BIN)

/*
 *  COMMUNICATION SEQUENCE:
 *  MASTER - > SENDS SYNC BYTE
 *  UNIT   - > READS ANDS RESPONDS WITH "SYNCED", FORGETS EVERY PREVIOUS BIT IN THE BUFFER
 *  MASTER - > SENDS CMD BYTE
 *  UNIT   - > READS AND REACTS, RETURNS CORRESPONDING STATE BYTE
 *  MASTER - > READS STATE BYTE, IF IT IS THE CORRECT ONE, TERMINATES COMM
 */

